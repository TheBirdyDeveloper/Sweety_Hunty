package candyLand;

public class LocationOnGrid {
	
	private int x;
	private int y;
	
	public LocationOnGrid(int posX, int posY) {
		this.x = posX;
		this.y = posY;
	}
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
}
